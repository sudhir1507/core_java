package abstraction;
/*
 * abstract class can have abstract methods
 * normal methods
 * abstract method does not have implementation in abstract class
 * implementation of abstract methods is done in child classes which are inheriting
 * abstract class 
 */
public abstract class Mobile {
	public abstract void doubleTap(); 
		
	public void abc() {
		System.out.println("i am abc");
	}
}
